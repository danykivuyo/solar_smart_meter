void delay_us(unsigned char time_us)
{
  unsigned char temp = 0;
  while (time_us--)
  {
    temp++;
    temp++;
    temp++;
    temp++;
    temp++;
  }
}
// 写入  最后将SDA拉高，以等待从设备产生应答
void IIC_write(unsigned char date)
{
  unsigned char i, temp;
  temp = date;

  for (i = 0; i < 8; i++)
  {
    digitalWrite(IIC_SCK, LOW);

    if ((temp & 0x80) == 0)
      digitalWrite(IIC_SDA, LOW);
    else
      digitalWrite(IIC_SDA, HIGH);
    temp = temp << 1;
    // 最少250ns延时
    digitalWrite(IIC_SCK, HIGH);
  }
  digitalWrite(IIC_SCK, LOW);
  digitalWrite(IIC_SDA, HIGH);
  digitalWrite(IIC_SCK, HIGH);
  // 不进行应答检测
  digitalWrite(IIC_SCK, LOW);
}
// 启动信号
// SCL在高电平期间，SDA由高电平向低电平的变化定义为启动信号
void IIC_start()
{
  digitalWrite(IIC_SDA, HIGH);
  digitalWrite(IIC_SCK, HIGH);
  // 所有操作结束释放SCL
  digitalWrite(IIC_SDA, LOW);
  digitalWrite(IIC_SCK, LOW);
  IIC_write(0x7E);
}

// 停止信号
// SCL在高电平期间，SDA由低电平向高电平的变化定义为停止信号
void IIC_stop()
{
  digitalWrite(IIC_SDA, LOW);
  digitalWrite(IIC_SCK, HIGH);
  digitalWrite(IIC_SDA, HIGH);
  digitalWrite(IIC_SCK, LOW);
  digitalWrite(IIC_SDA, LOW);
}

void LCD_send_cmd(unsigned char o_command)
{

  IIC_start();
  IIC_write(0x00);
  IIC_write(o_command);

  IIC_stop();
}
// 位逆序
unsigned char ReverseBits(unsigned char num)
{
  num = ((num & 0x0f) << 4) | ((num >> 4) & 0x0f);
  num = ((num & 0x33) << 2) | ((num >> 2) & 0x33);
  num = ((num & 0x55) << 1) | ((num >> 1) & 0x55);
  return num;
}

void LCD_send_data(unsigned char o_data)
{
  IIC_start();
  IIC_write(0x40);
  IIC_write(ReverseBits(o_data));

  IIC_stop();
}
void Column_set(unsigned char column)
{
  column = column + LCD_COLUMN_OFFSET;
  LCD_send_cmd(0x10 | (column >> 4));   // 设置列地址高位
  LCD_send_cmd(0x00 | (column & 0x0f)); // 设置列地址低位
}
void Line_set(unsigned char line)
{
  line = line + LCD_LINE_OFFSET;
  LCD_send_cmd(0x40 + line);
}
void Page_set(unsigned char page)
{
  LCD_send_cmd(0xb0 + page);
}
void LCD_clear(void)
{
  unsigned char page, column;
  for (page = 0; page < LCD_PAGE_NUMBER; page++)
  {
    Page_set(page);
    Column_set(0);
    for (column = 0; column < LCD_COLUMN_NUMBER; column++)
    {
      LCD_send_data(0x00);
    }
  }
}
void LCD_full(void)
{
  unsigned char page, column;
  for (page = 0; page < LCD_PAGE_NUMBER; page++) // page loop
  {
    Page_set(page);
    Column_set(0);
    for (column = 0; column < LCD_COLUMN_NUMBER; column++) // column loop
    {
      LCD_send_data(0xff);
    }
  }
}
void LCD_init(void)
{
  unsigned char i;
  for (i = 0; i < LCD_COMMAND_NUMBER; i++)
  {
    LCD_send_cmd(LCD_init_cmd[i]);
  }
}

