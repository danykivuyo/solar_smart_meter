void ClearCharacters(byte x, byte y, byte length)
{
  Page_set(7 - y);
  for (int i = 0; i < length; i++)
  {
    draw5x8((x + i) * 6, ' '); // Replace the character with a space
  }
}

void draw5x8(byte col, char b)
{
  Column_set(col);
  for (int n = 0; n < 5; n++)
  {
    LCD_send_data(pgm_read_byte(&font[b * 5 + n]));
  }
}

void IO_init(void)
{
  pinMode(IIC_SCK, OUTPUT); // 设置数字脚为输出
  pinMode(IIC_SDA, OUTPUT); // 设置数字脚为输出dddd
  digitalWrite(IIC_SCK, LOW);
  digitalWrite(IIC_SDA, LOW);
}

void lcd_init()
{
  IO_init();
  LCD_init();
  LCD_full();
  delay(1000);
}

